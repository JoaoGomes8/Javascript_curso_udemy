// statements - uma statement é uma instrução de código que executa uma ação

// definir uma variável éuma statement ou instrução
let text = "Olá mundo!";

// chamar uma função é uma statement ou instrução
console.log(text);

// instruções podem ser separadas por ponto e vírgula
console.log("Um"); console.log("Dois"); console.log("Três");

// instruções podem ser separadas por quebras de linha
console.log("Um")
console.log("Dois")
console.log("Três")

// as instruções podem ser separadas por ponto e virgula e quebras de linha
console.log("Um"); 
console.log("Dois"); 
console.log("Três");

// IMPORTANTE:
// Não usar ponto e virgula é possivel (mas não recomendado)
// Essa técnica de ausência de ponto e vírgula é chamada de ASI - Automatic Semicolon Insertion
// O JavaScript tenta inserir ponto e virgula automaticamente quando necessário.



